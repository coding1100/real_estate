import type { Metadata } from "next";
import { Suspense } from "react";
import {
  Bricolage_Grotesque,
  Alegreya_Sans,
  Poiret_One,
  Inter,
  Cormorant_Garamond,
  Source_Sans_3,
  Libre_Baskerville,
  Lato,
} from "next/font/google";
import type { EditorFontOption } from "@/lib/editorFonts";
import { getAdminUiSettings, getEnabledEditorFonts } from "@/lib/uiSettings";
import { prisma } from "@/lib/prisma";
import { withPrismaRetry } from "@/lib/prismaRetry";
import {
  getRequestHostnameFromHeaders,
  resolveTenantHostname,
} from "@/lib/hostnames";
import TopLoader from "@/components/shared/TopLoader";
import "./globals.css";

const headingFont = Source_Sans_3({
  variable: "--font-heading",
  subsets: ["latin"],
  weight: ["400", "500", "600", "700"],
});

const bodyFont = Source_Sans_3({
  variable: "--font-body",
  subsets: ["latin"],
  weight: ["400", "500", "600", "700"],
});

const displayFont = Bricolage_Grotesque({
  variable: "--font-bricolage",
  subsets: ["latin"],
  weight: ["400", "500", "600"],
});

const alegreyaSansFont = Alegreya_Sans({
  variable: "--font-alegreya-sans",
  subsets: ["latin"],
  weight: ["400"],
  style: ["normal", "italic"],
});

const poiretFont = Poiret_One({
  variable: "--font-poiret",
  subsets: ["latin"],
  weight: ["400"],
});

const interFont = Inter({
  variable: "--font-inter",
  subsets: ["latin"],
  weight: ["400", "500", "600", "700"],
});

const cormorantFont = Cormorant_Garamond({
  variable: "--font-cormorant-garamond",
  subsets: ["latin"],
  weight: ["400", "500", "600", "700"],
});

const libreBaskervilleFont = Libre_Baskerville({
  variable: "--font-libre-baskerville",
  subsets: ["latin"],
  weight: ["400", "700"],
});

const latoFont = Lato({
  variable: "--font-lato",
  subsets: ["latin"],
  weight: ["400", "700"],
});

const FALLBACK_FAVICON = "/engel-volkers-logo.svg";

export async function generateMetadata(): Promise<Metadata> {
  const rawHostname = await getRequestHostnameFromHeaders();
  const hostname = resolveTenantHostname(rawHostname);
  const hostCandidates = hostname.startsWith("www.")
    ? [hostname, hostname.slice(4)]
    : [hostname, `www.${hostname}`];

  const domain = await withPrismaRetry(() =>
    prisma.domain.findFirst({
      where: {
        hostname: { in: hostCandidates },
        isActive: true,
      },
      select: {
        faviconUrl: true,
      },
    }),
  );

  const iconUrl = domain?.faviconUrl || FALLBACK_FAVICON;

  return {
    title: "Create Next App",
    description: "Generated by create next app",
    icons: {
      icon: [{ url: iconUrl }],
      shortcut: [{ url: iconUrl }],
      apple: [{ url: iconUrl }],
    },
  };
}

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const { editorFonts } = await getAdminUiSettings();
  const enabledEditorFonts = getEnabledEditorFonts(editorFonts);
  const fontsWithImports = enabledEditorFonts.filter(
    (f: EditorFontOption) =>
      typeof f.importUrl === "string" && f.importUrl.trim().length > 0,
  );

  return (
    <html lang="en">
      <head>
        {fontsWithImports.map((font: EditorFontOption, index: number) => (
          <link
            key={index}
            rel="stylesheet"
            href={font.importUrl}
          />
        ))}
      </head>
      <body
        className={`${bodyFont.variable} ${headingFont.variable} ${displayFont.variable} ${alegreyaSansFont.variable} ${poiretFont.variable} ${interFont.variable} ${cormorantFont.variable} ${libreBaskervilleFont.variable} ${latoFont.variable} antialiased`}
      >
        <Suspense fallback={null}>
          <TopLoader />
        </Suspense>
        {children}
      </body>
    </html>
  );
}
